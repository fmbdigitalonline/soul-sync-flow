  // OpenAI API Integration via Edge Function
  private async callOpenAI(params: any): Promise<any> {
    try {
      console.log('ü§ñ Calling OpenAI via Edge Function...');
      console.log('üì§ Request params:', JSON.stringify(params, null, 2));
      
      const { data, error } = await supabase.functions.invoke('openai-agent', {
        body: {
          messages: params.messages,
          model: params.model || 'gpt-4o-mini',
          temperature: params.temperature || 0.7,
          tools: params.tools
        }
      });

      console.log('üì• Edge Function response - data:', JSON.stringify(data, null, 2));
      console.log('üì• Edge Function response - error:', error);

      if (error) {
        console.error('‚ùå Edge Function error:', error);
        throw new Error(`Agent API error: ${error.message}`);
      }

      if (!data) {
        console.error('‚ùå No data returned from Edge Function');
        throw new Error('No data returned from Edge Function');
      }

      if (!data.content) {
        console.error('‚ùå No content in response data:', data);
        throw new Error('No content returned from agent');
      }

      if (typeof data.content !== 'string' || data.content.trim() === '') {
        console.error('‚ùå Invalid content format:', typeof data.content, 'length:', data.content?.length);
        throw new Error('Invalid or empty content returned from agent');
      }

      // Format response to match expected structure
      return {
        choices: [{
          message: {
            content: data.content
          }
        }],
        usage: data.usage,
        model: data.model
      };
    } catch (error) {
      console.error('‚ùå OpenAI Agent call failed:', error);
      throw error;
    }
  }

  // Parsing Utilities
  private parseIntoBranches(content: string): ReasoningNode[] {
    // Simple parsing - in production, use more sophisticated parsing
    const branches: ReasoningNode[] = [];
    const lines = content.split('\n').filter(line => line.trim());
    
    for (let i = 0; i < Math.min(3, lines.length); i++) {
      branches.push({
        id: `branch_${i}`,
        thought: lines[i],
        confidence: 0.7 + (Math.random() * 0.3), // Placeholder
        children: []
      });
    }
    
    return branches;
  }

  private parseEvaluationScore(evaluation: string): number {
    // Extract numeric score from evaluation text
    const match = evaluation.match(/(\d+(?:\.\d+)?)/);
    return match ? parseFloat(match[1]) / 10 : 0.5;
  }

  private parsePlanFromDelivery(delivery: string): any {
    // Parse structured plan from delivery text
    // This would be more sophisticated in production
    return {
      formatted_content: delivery,
      extracted_actions: [],
      timeline: 'weekly',
      personalization_level: 'high'
    };
  }

